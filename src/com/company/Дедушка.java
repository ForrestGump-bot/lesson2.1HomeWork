package com.company;

public class Дедушка {

    private int age;
    private String name;
    private Position position;

    public Дедушка(int age, String name, Position position) {
        this.age = age;
        this.name = name;
        this.position = position;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public Position getPosition() {
        return position;
    }
}
